# execute insert
def db_insert(sql):
    try:
        db = pymysql.connect(host=config.MYSQL_HOST, user=config.MYSQL_USER,
                             password=config.MYSQL_PASSWORD, db=config.MYSQL_DB, charset='utf8', port=config.MYSQL_PORT)
        cur = db.cursor()
        cur.execute(sql)
        db.commit()
        db.close()
    except Exception as e:
        print('error, '+str(e))
# execute select


def db_select(sql):
    try:
        db = pymysql.connect(host=config.MYSQL_HOST, user=config.MYSQL_USER,
                             password=config.MYSQL_PASSWORD, db=config.MYSQL_DB, charset='utf8', port=config.MYSQL_PORT)
        cur = db.cursor()
        cur.execute(sql)
        results = cur.fetchall()  # 获取查询的所有记录
        db.close()
        return results
    except Exception as e:
        print('error, '+str(e))
